from itertools import combinations
import random

A = {1, 2, 3, 4, 5, 6}
n = 3
m = 5

combinations_list = []

A_list = list(A)

while len(combinations_list) < m:
    combination = set(random.sample(A_list, n))
    if combination not in combinations_list:
        combinations_list.append(combination)

print(combinations_list)



#В предоставленном вами коде вы генерируете случайные комбинации из n элементов из набора A и сохраняете их в списке, называемом combinations_list. Вот разбивка кода:

#из itertools импорт комбинаций: эта строка импортирует функцию комбинаций из модуля itertools. Однако функция комбинаций в коде не используется.
#import random: Эта строка импортирует модуль random, который используется для генерации случайных комбинаций.
#А = {1, 2, 3, 4, 5, 6}: Эта строка определяет набор под названием A с элементами 1, 2, 3, 4, 5, и 6.
#n = 3: В этой строке устанавливается значение n равным 3, которое представляет количество элементов для выбора в каждой комбинации.
#m = 5: В этой строке устанавливается значение m равным 5, которое представляет количество генерируемых комбинаций.
#combinations_list = []: Эта строка инициализирует пустой список с именем combinations_list для хранения сгенерированных комбинаций.
#A_list = список(A): Эта строка преобразует набор A в список, называемый A_list. Это сделано для облегчения случайной выборки из списка.
#Код вводит цикл while, который продолжается до тех пор, пока длина combinations_list не станет меньше m.
#Внутри цикла:
#Он генерирует случайную комбинацию, используя random.sample(A_list, n). Эта функция выбирает n элементов из списка A_list, создавая новый набор, называемый combination.
#Он проверяет, не находится ли комбинация еще в combinations_list. Если это не так, он добавляет комбинацию к combinations_list.
#Как только combinations_list содержит m уникальных комбинаций, цикл завершается.
#print(combinations_list): Наконец, в этой строке выводится список случайных комбинаций, хранящихся в combinations_list.
#Код эффективно генерирует и сохраняет m случайных комбинаций из n элементов из набора A, а затем печатает список этих комбинаций. Каждая комбинация представляет собой набор, содержащий n случайно выбранных элементов из набора A.